    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
         String course = (String) jComboBox2.getSelectedItem();
         String branch = (String) jComboBox1.getSelectedItem();
         String rollNumber = jTextField2.getText();
         String gender = (String) jComboBox3.getSelectedItem();
         String fatherName = jTextField1.getText();
         String name = jTextField3.getText();
         
         try {
             Class.forName("com.mysql.cj.jdbc.Driver");
             Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "1234");
             Statement st = con.createStatement();
             st.executeUpdate("insert into student(rollno, course, branch, name, gender, fathername) "
                     + "values('" + rollNumber + "', '" + course + "', '" + 
                     branch + "', '" + name + "', '" + gender + "' , '"+ fatherName +"')");
             JOptionPane.showMessageDialog(null, "Data Updated Successfully");
             setVisible(false);
             new index().setVisible(true);
         } 
         catch(Exception e){ 
             JOptionPane.showMessageDialog(null, e.toString());           
         }
    }//GEN-LAST:event_jButton6ActionPerformed

