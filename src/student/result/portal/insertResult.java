    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String rollNumber = jTextField7.getText();
        String physics = jTextField10.getText();
        String maths = jTextField8.getText();
        String em = jTextField9.getText();
        String dbms = jTextField12.getText();
        String os = jTextField11.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "1234");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno =  '" + rollNumber + "' ");
            if(rs.next()) {
                st.executeUpdate("insert into result(rollno, physics, maths, em, dbms, os) values('"+ rollNumber +"' , '"+ physics + "' ,'"+maths +"' , '" + em + "' , '" + dbms + "' , '" + os + "')");
                JOptionPane.showMessageDialog(null, "Result saved Sucessfully!");
                setVisible(false);
                new insertResult().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Student not registered!");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

